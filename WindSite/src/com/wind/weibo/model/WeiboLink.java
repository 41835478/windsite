package com.wind.weibo.model;

public class WeiboLink {
	private String link_name;
	private String link_address;

	/**
	 * @return the link_name
	 */
	public String getLink_name() {
		return link_name;
	}

	/**
	 * @param linkName
	 *            the link_name to set
	 */
	public void setLink_name(String linkName) {
		link_name = linkName;
	}

	/**
	 * @return the link_address
	 */
	public String getLink_address() {
		return link_address;
	}

	/**
	 * @param linkAddress
	 *            the link_address to set
	 */
	public void setLink_address(String linkAddress) {
		link_address = linkAddress;
	}

}
